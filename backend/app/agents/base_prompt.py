    base          = BASE_SYSTEM_PROMPT
    thinking      = THINKING_LAYERS.get(mode, THINKING_LAYERS["standard"])
    agent_specific = AGENT_PROMPTS.get(agent_type, AGENT_PROMPTS["general"])
    return base + thinking + agent_specific + (f"\n{extra}" if extra else "")